    private void jButtonCreateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCreateMouseClicked
        // Gather information from text boxes.
        int flag = 0;
        String social = socialText.getText();
        System.out.println(social);
        String fName = firstName.getText();
        System.out.println(fName);
        String lName = lastName.getText();
        System.out.println(lName);
        String username = UserName.getText();
        System.out.println(username);
        String pssword = Password.getText();
        System.out.println(pssword);
        //int date = DateBox.getDate();
        String position = Positiontext.getText();
        if(position.equals("manager")){
            position = "1";
        }
        else if(position.equals("server")){
            position = "2";
        }
        else if(position.equals("cook")){
            position = "3";
        }
        else if(position.equals("busser")){
            position = "4";
        }
        else if(position.equals("dishwasher")){
            position = "5";
        }
        else{
            position = "6";
        }
        
        String addy = Address.getText();
        System.out.println(addy);

        flag = LoginAndRegister.createAccount(social, fName, lName, username, pssword, position, addy);
        if(flag==1){
            RestaurantApp ra = new RestaurantApp();
            ra.setVisible(true);
            ra.pack();
            ra.setLocationRelativeTo(null);
            ra.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonCreateMouseClicked

